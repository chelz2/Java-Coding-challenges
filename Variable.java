/**
 * Variable & data-tpyes in java - int, long, double, string, char 
 * Math operations in java
 * 
 */
public class Variable {
    public static void main(String[] args) {
        // int data type
        int apple = 5;
        System.out.println(apple);

        // string 
        String first_Name = "Man";
        String last_Name = "Hoe";
        String full_Name = "full name is " + first_Name + last_Name;

        System.out.println(full_Name);

        // char
        char mathGrade = 'A';
        System.out.println("Math Grade: " + mathGrade);
    }
}